#estes valores, mas imprima na tela informando qual deles é o MAIOR.



num1 = int(input("Digite 1° Número: ")) 
num2 = int(input("Digite 2° Número: ")) 

if num1 > num2:
    print(f'\nNúmero maior é {num1}\n')
else:
    print(f'\nNúmero maior é {num2}\n')